#    of G11: the numerator should read -(x₁x₂)⁹y₁¹⁰(y₂y₃)⁵z₁⁶(z₂z₃z₄)⁴t²,
#    and in the last  product in the denominator i should run in {2,3}.
# -- there is a misprint in the numerator of the relative degrees of the 
#    3-dimensional characters of G19: it should read x₁¹⁰x₂¹⁵y₁⁷z₁³z₄¹²

const G4_22IndexChars_dict=Dict{Int,Any}((i=>Dict() for i in 4:22)...)

CHEVIE.CheckIndexChars=false

function G4_22FetchIndexChars(ST, para)
  if !CHEVIE.CheckIndexChars
    return chevieget(:G4_22, :CharInfo)(ST)[:indexchars]
  end
  get!(G4_22IndexChars_dict[ST],para)do
    chevieget(:G4_22, :HeckeCharTable)(ST, para,[])[:indexchars]
  end
end

